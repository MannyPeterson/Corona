	private void keySchedule(int[] k) {
		RK = new int[NR][BC];

		int r, s;

		for(r = 0; r < NR; r++) {
			RK[r][0] = SK[r][k[r]];
			for(s = 1; s < BC; s++) {
				RK[r][s] = (2 * RK[r][s - 1]) % 256 + RK[r][s - 1] / 128;
				RK[r][s] = SK[r][RK[r][s]];
			}
		}
	}
	public Corona(int[] k) {
		BC = 24;

		if(k.length == 24) {
			NR = 24;
		} else if(k.length == 28) {
			NR = 28;
		} else if(k.length == 32) {
			NR = 32;
		}

		initPBoxes();

		initSBoxes1();
		initSBoxes2();

		keySchedule(k);
	}
	public int[] encrypt(int[] p) {
		int[] a = new int[BC];
		int[] b = new int[BC];

		int r, s;

		// Add Round Key
		for(s = 0; s < BC; s++) {
			a[s] = p[s] ^ RK[0][s];
		}

		for(r = 1; r < NR; r++){
			// Byte Transposition
			for(s = 0; s < BC; s++) {
				b[P[s]] = a[s];
			}

			// Multiply Over GF(2^16)
			for(s = 0; s < BC; s += 2) {
				a[s] = (int) (((((b[s] * 256) + b[s + 1]) * M[s / 2]) % 65536) / 256);
				a[s + 1] = (int) (((((b[s] * 256) + b[s + 1]) * M[s / 2]) % 65536) % 256);
			}

			// Right Rotate
			for(s = 0; s < BC; s++) {
				a[s] = (128 * a[s]) % 256 + a[s] / 2;
			}

			// Byte Subtituion
			for(s = 0; s < BC; s++) {
				a[s] = S[s % 3][a[s]];
			}

			// Add Round Key
			for(s = 0; s < BC; s++) {
				a[s] = a[s] ^ RK[r][s];
			}
		}

		return a;
	}
	public int[] decrypt(int[] c) {
		int[] a = new int[BC];
		int[] b = new int[BC];

		int r, s;

		for(s = 0; s < BC; s++) {
			a[s] = c[s];
		}

		for(r = NR - 1; r > 0; r--) {
			// Add Round Key
			for(s = 0; s < BC; s++) {
				a[s] = a[s] ^ RK[r][s];
			}

			// Inverse Byte Subtituion
			for(s = 0; s < BC; s++) {
				a[s] = SI[s % 3][a[s]];
			}

			// Left Rotate
			for(s = 0; s < BC; s++) {
				a[s] = (2 * a[s]) % 256 + a[s] / 128;
			}

			// Multiply Inverses Over GF(2^16)
			for(s = 0; s < BC; s += 2) {
				b[s] = (int) ((((a[s] * 256) + a[s + 1]) * MI[s / 2] % 65536) / 256);
				b[s + 1] = (int) ((((a[s] * 256) + a[s + 1]) * MI[s / 2] % 65536) % 256);
			}

			// Byte Transposition
			for(s = 0; s < BC; s++) {
				a[s] = b[P[s]];
			}
		}

		// Add Round Key
		for(s = 0; s < BC; s++) {
			a[s] = a[s] ^ RK[0][s];
		}

		return a;
	}
